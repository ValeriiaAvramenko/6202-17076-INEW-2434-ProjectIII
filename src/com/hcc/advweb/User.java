package com.hcc.advweb;

public class User {
private String login;
private String hours;
private String id;
public User() {}
public String getLogin() {
	return login;
}
public void setLogin(String login) {
	this.login = login;
}
public String getHours() {
	return hours;
}
public void setHours(String hours) {
	this.hours = hours;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
}
